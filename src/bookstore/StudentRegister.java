    public boolean checkIdNumber(String idnumber){
        PreparedStatement ps;
        ResultSet rs;
        boolean checkid = false;
        String sql = "Select * from BOOK.USERS Where IDNUMBER=?";
        try{
            ps = con.prepareStatement(sql);
            ps.setString(1, idnumber);
            rs = ps.executeQuery();
            if(rs.next()){
                checkid = true;
            }
        }
        catch(SQLException e){
            Logger.getLogger(StudentRegister.class.getName()).log(Level.SEVERE, null, e);
        }
        return checkid;
    }
    
