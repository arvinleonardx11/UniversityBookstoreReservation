    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        // TODO add your handling code here:
        String idnumber = jTextField1.getText();
        String firstname = jTextField2.getText();
        String lastname = jTextField3.getText();
        String password = jPasswordField1.getText();
        if(idnumber.equals("")){
            JOptionPane.showMessageDialog(null, "Input ID Number");
        }
        else if(checkIdNumber(idnumber)){
            JOptionPane.showMessageDialog(null, "ID Number already exists");
        }
        else if(firstname.equals("")){
            JOptionPane.showMessageDialog(null, "Input First Name");
        }
        else if(lastname.equals("")){
            JOptionPane.showMessageDialog(null, "Input Last Name");
        }
        else if(jComboBox1.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Select Course");
        }
        else if(password.equals("")){
            JOptionPane.showMessageDialog(null, "Input Password");
        }
        else{
            String course = jComboBox1.getSelectedItem().toString();
            try{
                PreparedStatement add = con.prepareStatement("insert into BOOK.USERS values (?,?,?,?,?)");
                add.setString(1, idnumber);
                add.setString(2, firstname);
                add.setString(3, lastname);
                add.setString(4, course);
                add.setString(5, password);
                int row = add.executeUpdate();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jPasswordField1.setText("");
                jComboBox1.setSelectedIndex(-1);
                JOptionPane.showMessageDialog(null, "Account Successfully Created");
                
            }
            catch(SQLException e){
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_jLabel3MouseClicked

